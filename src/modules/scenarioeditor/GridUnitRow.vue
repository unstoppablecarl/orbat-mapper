<script setup lang="ts">
import { ChevronRightIcon } from "@heroicons/vue/20/solid";
import type { TableColumn, TableColumnWidth } from "@/modules/scenarioeditor/types";
import type { NUnit } from "@/types/internalModels";
import MilSymbol from "@/components/MilSymbol.vue";
import GridEditableCell from "@/modules/scenarioeditor/GridEditableCell.vue";

interface Props {
  unit: NUnit;
  itemIndex: number;
  level: number;
  columns: TableColumn[];
  widths: TableColumnWidth;
  isActive: boolean;
}

const props = defineProps<Props>();
const emit = defineEmits([
  "toggle",
  "expand",
  "updateUnit",
  "nextCell",
  "activeItem",
  "edit",
]);

function toggleOpen() {
  props.unit._isOpen = !props.unit._isOpen;
}
</script>
<template>
  <div
    role="row"
    :id="`item-${unit.id}`"
    class="flex divide-x divide-gray-200 hover:bg-gray-100"
  >
    <div class="relative" role="gridcell">
      <div v-if="isActive" class="absolute inset-y-0 right-0 w-0.5 bg-indigo-600"></div>
    </div>
    <div class="" role="gridcell" style="width: 100px">
      <div
        :id="`cell-${itemIndex}-0`"
        class="flex items-center whitespace-nowrap border-2 border-white py-3 text-sm text-gray-900 outline-0 focus-within:border-red-800"
        :style="`padding-left: ${level + 1}rem`"
        tabindex="0"
        @keydown.enter.exact="toggleOpen()"
        @click.self="toggleOpen()"
      >
        <button v-if="unit.subUnits.length" @click="toggleOpen()">
          <ChevronRightIcon
            class="h-6 w-6 transform text-gray-500 transition-transform group-hover:text-gray-900 dark:text-gray-400 dark:group-hover:text-gray-100"
            :class="{
              'rotate-90': unit._isOpen,
            }"
          />
        </button>
        <MilSymbol
          :sidc="unit.sidc"
          class="ml-2"
          :class="{ 'ml-8': !unit.subUnits.length }"
        />
        <button class="ml-2 truncate hover:underline">{{ unit.name }}</button>
      </div>
    </div>
    <div
      v-for="(column, colIndex) in columns"
      role="gridcell"
      :key="column.value"
      class="dummy"
      :style="{ width: widths[column.label] }"
    >
      <GridEditableCell
        :value="unit[column.value]"
        :row-index="itemIndex"
        :col-index="colIndex + 1"
        :cell-type="column.type"
        @update="emit('updateUnit', unit.id, { [column.value]: $event })"
        @next-cell="emit('nextCell', $event)"
        @active="emit('activeItem', column.value)"
        @edit="emit('edit', unit, column.value, $event)"
      >
      </GridEditableCell>
    </div>
  </div>
</template>
